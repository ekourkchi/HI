In this project, I am compiling the next generation of Cosmicflows distances (Cosmicflows-4) that consists of ~35,000 galaxies with accurately measured distances. This is the most complete distance catalog of galaxies by far. Then with the aid of our team members, I am using this catalog to measure the local flows of local galaxies, using which we are carefully mapping the underlying structures, like voids and over-dense structures. Providing such a high quality big catalog of galaxy distances increases the resolution of all previous maps, with covering more regions for which we did not have any previous data, and it extends our maps far beyond any other studies so far. This ambitious project tries to deepen our vision to our local Universe far beyond our home supercluster: Laniakea. Surprisingly, regarding the visualization products of this project (videos, maps and graphs), there is only minor differentiation between what we publish in scientific journals and what is created for public consumptions. </div>
